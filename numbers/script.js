
function sumThreeNumbers(num1, num2, num3) {
    return num1 + num2 + num3;
}


function differenceTwoNumbers(num1, num2) {
    return num1 - num2;
}


function productTwoNumbers(num1, num2) {
    return num1 * num2;
}


function averageTwoNumbers(num1, num2) {
    return (num1 + num2) / 2;
}

console.log(sumThreeNumbers(5, 20, 20));  
console.log(differenceTwoNumbers(20, 5));  
console.log(productTwoNumbers(5, 5));      
console.log(averageTwoNumbers(30, 50));    
